#include "../headers/lau.h"

bool is_zero(int16_t v) { return v == 0; }

bool is_neg(int16_t v) { return v <= 0; }

int16_t add(int16_t a, int16_t b) { return a + b; }

int16_t sub(int16_t a, int16_t b) { return a - b; }

int16_t mul(int16_t a, int16_t b) { return a * b; }

int16_t div(int16_t a, int16_t b) { return a / b; }
